def heatmap_to_mask_safe(heatmap, threshold=0.5):
    mask = (heatmap >= threshold).astype(np.uint8) * 255

    kernel = np.ones((3, 3), np.uint8)
    mask = cv2.morphologyEx(mask, cv2.MORPH_OPEN, kernel)
    mask = cv2.morphologyEx(mask, cv2.MORPH_CLOSE, kernel)

    return mask

def display_mask_overlay_safe(img_path, threshold=0.5):
    # ここは safe版 Grad-CAM を使う
    img_pil, img_uint8, heatmap, pred = make_gradcam_heatmap_safe(img_path)

    # heatmap を入力画像サイズに合わせる
    heatmap_resized = cv2.resize(
        heatmap,
        (img_uint8.shape[1], img_uint8.shape[0])
    )

    mask = heatmap_to_mask_safe(heatmap_resized, threshold=threshold)

    overlay = img_uint8.copy()
    contours, _ = cv2.findContours(mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    cv2.drawContours(overlay, contours, -1, (255, 0, 0), 2)

    plt.figure(figsize=(16, 4))

    plt.subplot(1, 4, 1)
    plt.imshow(img_uint8)
    plt.title(f"Input\nPred={pred:.3f}")
    plt.axis("off")

    plt.subplot(1, 4, 2)
    plt.imshow(heatmap_resized, cmap="jet")
    plt.title("Heatmap")
    plt.axis("off")

    plt.subplot(1, 4, 3)
    plt.imshow(mask, cmap="gray")
    plt.title("Estimated Mask")
    plt.axis("off")

    plt.subplot(1, 4, 4)
    plt.imshow(overlay)
    plt.title("Contour Overlay")
    plt.axis("off")

    plt.tight_layout()
    plt.show()

    return heatmap_resized, mask, overlay, pred


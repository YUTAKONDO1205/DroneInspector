def load_image_raw(img_path, target_size=IMG_SIZE):
    img = tf.keras.utils.load_img(img_path, target_size=target_size)
    arr = tf.keras.utils.img_to_array(img).astype("float32")  # 0..255
    arr = np.expand_dims(arr, axis=0)
    return img, arr


def make_gradcam_heatmap_safe(img_path):
    img_pil, img_raw = load_image_raw(img_path, target_size=IMG_SIZE)

    # MobileNetV2 用の正しい前処理
    img_pp = tf.keras.applications.mobilenet_v2.preprocess_input(img_raw.copy())

    with tf.GradientTape() as tape:
        last_conv_output = last_conv_layer_model(img_pp, training=False)
        tape.watch(last_conv_output)

        preds = classifier_model(last_conv_output, training=False)
        class_score = preds[:, 0]  # sigmoid binary output

    grads = tape.gradient(class_score, last_conv_output)

    # mixed precision対策も兼ねて float32 にそろえる
    last_conv_output = tf.cast(last_conv_output, tf.float32)
    grads = tf.cast(grads, tf.float32)

    pooled_grads = tf.reduce_mean(grads, axis=(0, 1, 2))
    conv_output = last_conv_output[0]  # (H, W, C)

    heatmap = tf.reduce_sum(conv_output * pooled_grads, axis=-1)
    heatmap = tf.maximum(heatmap, 0)
    heatmap = heatmap / (tf.reduce_max(heatmap) + 1e-8)

    return img_pil, img_raw[0].astype("uint8"), heatmap.numpy(), float(preds[0, 0])


def display_gradcam_safe(img_path, alpha=0.4):
    img_pil, img_uint8, heatmap, pred = make_gradcam_heatmap_safe(img_path)

    heatmap_uint8 = np.uint8(255 * heatmap)

    cmap = plt.get_cmap("jet")
    jet_colors = cmap(np.arange(256))[:, :3]
    jet_heatmap = jet_colors[heatmap_uint8]
    jet_heatmap = tf.keras.utils.array_to_img(jet_heatmap)
    jet_heatmap = jet_heatmap.resize((img_uint8.shape[1], img_uint8.shape[0]))
    jet_heatmap = tf.keras.utils.img_to_array(jet_heatmap)

    overlay = jet_heatmap * alpha + img_uint8
    overlay = np.clip(overlay / 255.0, 0, 1)

    plt.figure(figsize=(12, 4))

    plt.subplot(1, 3, 1)
